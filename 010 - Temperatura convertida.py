'''Escreva um programa que converta uma
temperatura digitada em ºC converta para ºF.'''

temperaturaC = float(input("Informe a temperatura em ºC:"))
Farenheit = ((9 * temperaturaC) / 5 ) + 32
print("Temperatura em ºC é: {}ºC".format(temperaturaC))
print("Temperatura em ºF é: {}ºF".format(Farenheit))
